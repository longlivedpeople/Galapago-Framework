class modelSet:

    def __init__(self):

        self.eleFiles = []
        self.muFiles = []
        self.mHs = []
        self.mXs = []
        self.ctaus = []
        self.number = 0

    def addModel(self, eleFile, muFile, mH, mX, ctau):

        self.eleFiles.append(eleFile)
        self.muFiles.append(muFile)
        self.mHs.append(mH)
        self.mXs.append(mX)
        self.ctaus.append(ctau)
        self.number+=1


