def makePlot(lumi, treeSI, treeMC, var, name, xlabel, logx, LLlabel = False, normed = False):

    hMCS = treeMC.getLoopStack('histMC_'+name, var, xlabel)
    hSIS = treeSI.getLoopStack('histSI_'+name, var, xlabel)

    luminosity = lumi

    ### MC total histogram
    f = 0
    for _i, _h in enumerate(hMCS.GetHists()):
        if not f: hMC = copy.deepcopy(_h)
        else: hMC.Add(_h, 1.)
        f = 1

    #hMC.GetXaxis().SetTitle(hMCS.GetXaxis().GetTitle())

    ### Signal histograms
    s_histos = []
    for _i, _h in enumerate(hSIS.GetHists()):
        s_histos.append(copy.deepcopy(_h))

    # Normalization
    if normed:
        hMC.Scale(1.0/hMC.Integral())
        for _h in s_histos: _h.Scale(1.0/_h.Integral())
        luminosity = 1.0

    ### Get maximum
    maxValMC = hMC.GetMaximum()
    maxValSI = max([s_histos[i].GetMaximum() for i in range(0, len(s_histos))])
    maxVal = max(maxValMC, maxValSI)

    ### Set Maximum
    if not logx:
        hMCS.SetMaximum(1.3*maxVal)
        hMCS.SetMinimum(0.0)
        hMC.SetMaximum(1.3*maxVal)
        hMC.SetMinimum(0.0)
        for _h in s_histos: 
            _h.SetMaximum(1.3*maxVal)
            _h.SetMinimum(0.0)
    else:
        hMCS.SetMaximum(10.0*maxVal)
        hMCS.SetMinimum(0.1)
        hMC.SetMaximum(10.0*maxVal)
        hMC.SetMinimum(0.1)
        for _h in s_histos: 
            _h.SetMaximum(10.0*maxVal)
            _h.SetMinimum(0.1)


    ### Canvas object
    plot = Canvas.Canvas('hist_'+name, 'png', 0.62, 0.73, 0.9, 0.9)

    if normed: plot.addHisto(hMC, 'HIST', '', 'l', r.kBlue, 1, 0) # Background
    else: plot.addStack(hMCS, 'HIST', 1, 0) # Background

    for _h in s_histos:
        _h.Scale(1000.0)
        plot.addHisto(_h, 'HIST, SAME', '', 'l', _h.GetFillColor(), 1, 3) # Signal

    ### Dilepton banner
    if LLlabel == 'EE':
        plot.addLatex(0.17, 0.86, 'e^{+}e^{-} channel')
    if LLlabel == 'MM':
        plot.addLatex(0.17, 0.86, '#mu^{+}#mu^{-} channel')

    ### Save it
    plot.save(1, 0, logx, luminosity, '', outputDir = WORKPATH + 'plots/')


def makeSensitivity(lumi, treeSI, treeMC, var, name, xlabel, logx, LLlabel = False, normed = False):

    hMCS = treeMC.getLoopTH1F('histMC_'+name, var, xlabel)
    hSIS = treeSI.getLoopStack('histSI_'+name, var, xlabel)

    luminosity = lumi

    ### Signal histograms
    s_histos = []
    for _i, _h in enumerate(hSIS.GetHists()):
        s_histos.append(copy.deepcopy(_h))

    ### Get significance values
    plot = Canvas.Canvas('sensitivity_'+name, 'png', 0.6, 0.6, 0.9, 0.9)
    for _i, _h in enumerate(s_histos):
        gh = r.TH1F('gr', '', hMCS.GetNbinsX(), hMCS.GetXaxis().GetXmin(), hMCS.GetXaxis().GetXmax())
        for n in range(1, hMCS.GetNbinsX() + 1):
            if _h.GetBinContent(n) == 0: 
                value = 0.0
            else:
                value = _h.GetBinContent(n)/math.sqrt(_h.GetBinContent(n) + hMCS.GetBinContent(n))
            gh.SetBinContent(n, value)

        gh.SetTitle(_h.GetTitle())
        gh.SetLineWidth(2)
        gh.GetYaxis().SetTitle('S/#sqrt{S + B}')
        gh.GetXaxis().SetTitle(xlabel)
        gh.SetMaximum(5)
        gh2 = copy.deepcopy(gh)
        gr = r.TGraph(gh2)

        if _i == 0:
            plot.addHisto(gh2, 'l', _h.GetTitle(), 'l', _h.GetFillColor(), 1, 1)
        else:
            plot.addHisto(gh2, 'l, same', _h.GetTitle(), 'l', _h.GetFillColor(), 1, 1)
            
    ### Dilepton banner
    if LLlabel == 'EE':
        plot.addLatex(0.17, 0.86, 'e^{+}e^{-} channel')
    if LLlabel == 'MM':
        plot.addLatex(0.17, 0.86, '#mu^{+}#mu^{-} channel')

    ### Save it
    plot.save(1, 0, logx, luminosity, '', outputDir = WORKPATH + 'sensitivity/')



